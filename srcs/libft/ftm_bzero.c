#include "libft_malloc.h"

void	ftm_bzero(void *s, size_t n)
{
	size_t	i;

	i = 0;
	if (!(s) || !(n))
		return ;
	while (i < n)
	{
		((char *)s)[i] = 0;
		i++;
	}
}
